@implementation AppDelegate

@synthesize window = _window;
@synthesize tabBarController = _tabBarController;

- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions
{
    self.window = [[UIWindow alloc] initWithFrame:[[UIScreen mainScreen] bounds]];
    // Override point for customization after application launch.
    UIViewController *viewController1 = [[SystemSoundVC alloc] initWithNibName:@"SystemSoundVC" bundle:nil];
    UIViewController *viewController2 = [[MediaPickerVC alloc] initWithNibName:@"MediaPickerVC" bundle:nil];
    UIViewController *viewController3 = [[AVAudioPlayerVC alloc] initWithNibName:@"AVAudioPlayerVC" bundle:nil];
    self.tabBarController = [[UITabBarController alloc] init];
    self.tabBarController.viewControllers = [NSArray arrayWithObjects:viewController1, viewController2,viewController3, nil];
    self.window.rootViewController = self.tabBarController;
    [self.window makeKeyAndVisible];
    return YES;
}

